package org.java;

public class Static {
	public void mo() {
		System.out.println("Fine");
	}

	static String a = "I am";
	static class String1{
	public static void forr() {
		System.out.println("Great");
		
	}
	}
	public static void main(String[] args) {
		Static c = new Static();
		c.mo();
		String1.forr();
		System.out.println();
		System.out.println(a);
		
	}

}

